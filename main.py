# Preamble, SAME header, and attention signals
def generate_audio_data(type, sample_rate):
    # Constants
    mark_space_time = 0.00192  # Mark and space time in seconds (1.92 ms)
    mark_frequency = 2083.3  # Mark frequency (2083.3 Hz)
    space_frequency = 1562.5  # Space frequency (1562.5 Hz)
    preamble = '11010101110101011101010111010101110101011101010111010101110101011101010111010101110101011101010111010101110101011101010111010101' # 16 bytes of hexadecimal AB for system clearing and clock synchronization
    same_header = same_header_preview_var.get() # Get SAME header as constant
    ebs_attn_signal = ebs_attn_signal_var.get() # Get EBS "Two-Tone" Attention Signal (853 + 960 Hz) checkbox variable as constant
    nwr_wat = nwr_wat_var.get() # Get NWR Warning Alarm Tone (1050 Hz) checkbox variable as constant
    ebs_attn_signal_duration = int(ebs_attn_signal_duration_spinbox.get()) # Get entered duration of EBS "Two-Tone" Attention Signal in seconds
    nwr_wat_duration = int(nwr_wat_duration_spinbox.get()) # Get entered duration of NWR Warning Alarm Tone in seconds

    data = np.array([]) # Create empty array

    if type == 0: # Check if function was called for SAME headers and optional attention signals
        bitstream = preamble + ''.join(format(ord(c), '08b')[::-1] for c in same_header) # Convert SAME header from ASCII to binary LSB0 and append it to the preamble
    else: # If function was called for end of message (EOM) footers
        bitstream = preamble + '01110010011100100111001001110010' # Concatenate preamble with "NNNN"
    
    for i in range(3): # Repeat 3 times for 3 data bursts
        for bit in bitstream: # Repeat for every bit in the concatenation (bitstream)
            # Set the frequency based on the bit (0 or 1)
            if bit == '1': # Check if the bit is a mark
                frequency = mark_frequency # Mark frequency (2083.3 Hz)
            else: # If the bit is a space
                frequency = space_frequency # Space frequency (1562.5 Hz)

            t = np.linspace(0, mark_space_time, int(sample_rate * mark_space_time), endpoint=False) # Create the time vector for the bit duration
            wave = np.sin(2 * np.pi * frequency * t) # Generate the sine wave at the set frequency within the time vector
            data = np.concatenate((data, wave)) # Append the wave to the signal

        # Add 1-second silent delay
        t = np.linspace(0, 1, int(sample_rate), endpoint=False) # Create the time vector for 1 second
        wave = np.sin(2 * np.pi * 0 * t) # 0 Hz wave for 1 second
        data = np.concatenate((data, wave)) # Append the silent delay to the signal

    if type == 0: # Check if function was called for SAME headers and optional attention signals
        if ebs_attn_signal: # Check if EBS "Two-Tone" Attention Signal (853 + 960 Hz) checkbox was checked
            # Constants
            frequency1 = 853 # Lower frequency of the sine wave (853 Hz)
            frequency2 = 960 # Higher frequency of the sine wave (960 Hz)

            t = np.linspace(0, ebs_attn_signal_duration, int(sample_rate * ebs_attn_signal_duration), endpoint=False) # Create the time vector for the entered duration of the EBS "Two-Tone" Attention Signal
            wave1 = np.sin(2 * np.pi * frequency1 * t) # Generate the lower frequency (853 Hz) sine wave within the time vector
            wave2 = np.sin(2 * np.pi * frequency2 * t) # Generate the higher frequency (960 Hz) sine wave within the time vector
            mixed_wave = (wave1 + wave2) / 2  # Mix the two waves and normalize amplitude to prevent clipping
            data = np.concatenate((data, mixed_wave)) # Append the mixed wave to the signal

            # Add 1-second silent delay
            t = np.linspace(0, 1, int(sample_rate), endpoint=False) # Create the time vector for 1 second
            wave = np.sin(2 * np.pi * 0 * t) # 0 Hz wave for 1 second
            data = np.concatenate((data, wave)) # Append the silent delay to the signal
            
        if nwr_wat: # Check if NWR Warning Alarm Tone (1050 Hz) checkbox was checked
            # Constants
            frequency = 1050  # Frequency of the sine wave (1050 Hz)

            t = np.linspace(0, nwr_wat_duration, int(sample_rate * nwr_wat_duration), endpoint=False) # Create the time vector for the entered duration of the NWR Warning Alarm Tone
            wave = np.sin(2 * np.pi * frequency * t)  # Generate the sine wave at 1050 Hz within the time vector
            data = np.concatenate((data, wave)) # Append the wave to the signal

            # Add 1-second silent delay
            t = np.linspace(0, 1, int(sample_rate), endpoint=False) # Create the time vector for 1 second
            wave = np.sin(2 * np.pi * 0 * t) # 0 Hz wave for 1 second
            data = np.concatenate((data, wave)) # Append the silent delay to the signal
    
    return data

