# Add selected location to location list
def add_location():
    selected_country = country_combobox.get() # Get selected country
    state_code = STATES.get(selected_country, {}).get(state_combobox.get(), "") # Get selected state/province/territory
    region_code = REGIONS.get(state_code, {}).get(region_combobox.get(), "") # Get selected region (county/county-equivalent/forecast region)
    subdivision_code = SUBDIVISIONS.get(subdivision_combobox.get()) # Get selected subdivision

    if state_code and region_code and subdivision_code: # Check if state/province/territory code, region (county/county-equivalent/forecast region) code, and subdivision code exist
        if ({subdivision_combobox.get()}, {region_combobox.get()}, {state_combobox.get()}) not in location_list: # Prevent duplicate entries
            same_location_code = f"{subdivision_code}{state_code}{region_code}" # Concatenate full 6-digit SAME location code

            # Add to location lists
            location_list.append(({subdivision_combobox.get()}, {region_combobox.get()}, {state_combobox.get()})) # Add user-readable location to user-readable location list
            same_location_list.append(same_location_code) # Add full 6-digit SAME location code to SAME location list
            location_table.insert("", "end", values=(subdivision_combobox.get(), region_combobox.get(), state_combobox.get())) # Add user-readable location to user-readable location GUI table
            update_same_header(()) # Update SAME header

# Delete selected location(s) from location list
def delete_location():
    selected_location = location_table.selection() # Get selected location(s)

    if selected_location: # Check if selected location(s) exist
        for location in selected_location: # Repeat for every selected location
            values = location_table.item(location, "values") # Get list of location parts
            subdivision, region, state = values # Split list of location parts into separate variables

            # Reconstruct SAME location code for removal
            selected_country = country_combobox.get() # Get selected country
            state_code = STATES.get(selected_country, {}).get(state, "") # Get selected state/province/territory
            region_code = REGIONS.get(state_code, {}).get(region, "") # Get selected region (county/county-equivalent/forecast region)
            subdivision_code = SUBDIVISIONS.get(subdivision, "") # Get selected subdivision

            if state_code and region_code and subdivision_code: # Check if state/province/territory code, region (county/county-equivalent/forecast region) code, and subdivision code exist
                same_location_code = f"{subdivision_code}{state_code}{region_code}" # Concatenate full 6-digit SAME FIPS code
                
                # Remove from location lists
                location_list.remove(({subdivision}, {region}, {state})) # Remove from user-readable location list
                same_location_list.remove(same_location_code) # Remove from SAME location code list
                location_table.delete(location) # Remove from user-readable location GUI table

        update_same_header(()) # Update SAME header

# Generate purge time values in required increments (00:15, 00:30, etc.)
def generate_time_values():
    time_values = []
    for hour in range(100):  # 0-99 hours
        if hour == 99:
            minute_intervals = [0, 30]  # 99:30 maximum
        elif hour < 1:
            minute_intervals = range(0, 60, 15)  # 15-minute increments for < 1 hour
        else:
            minute_intervals = range(0, 60, 30)  # 30-minute increments for >= 1 hour
        
        for minute in minute_intervals:
            time_values.append(f"{hour:02}:{minute:02}") # Format HH:MM
    
    return time_values

# Validate callsign input: limit to 8 characters and exclude dashes (hyphens)
def validate_callsign_input(P):
    if all(32 <= ord(c) <= 126 for c in P) and '-' not in P and len(P) <= 8 : # Checks if entered character is a 7-bit ASCII character and is not a dash (hyphen), and if entered callsign is not yet 8 characters long
        return True # Accepts entered character
    else:
        return False # Rejects entered character

